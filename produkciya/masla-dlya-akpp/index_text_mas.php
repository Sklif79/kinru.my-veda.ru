<h1 style="text-align: justify;">Масла для АКПП<br>
 </h1>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 Масло для автоматической коробки передач (ATF) является одним из самых специфических продуктов автохимии, поскольку автоматические коробки передач включают в себя несколько совершенно разных по специфике работы узлов - гидротрансформатор, шестеренчатые планетарные передачи, сложную систему гидравлического управления. По этим причинам спектр функций ATF очень велик: она смазывает, отводит лишнее тепло от деталей, выступает в качестве рабочего тела для обеспечения автоматизации работы АКПП, защищает от коррозии и износа.
